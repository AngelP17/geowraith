type Climate = 'tropical' | 'arid' | 'temperate' | 'cold' | 'polar';
type UrbanDensity = 'dense' | 'moderate' | 'sparse';

export interface City {
  id: string;
  name: string;
  lat: number;
  lon: number;
  climate: Climate;
  urbanDensity: UrbanDensity;
}

function inferClimate(lat: number): Climate {
  const absLat = Math.abs(lat);
  if (absLat < 23.5) return 'tropical';
  if (absLat < 35) return 'arid';
  if (absLat < 55) return 'temperate';
  if (absLat < 66.5) return 'cold';
  return 'polar';
}

function toCityCompat(rec: CityRecord, idx: number): City {
  return {
    id: `city_${idx}`,
    name: `${rec.city}, ${rec.country}`,
    lat: rec.lat,
    lon: rec.lon,
    climate: inferClimate(rec.lat),
    urbanDensity: 'moderate',
  };
}

